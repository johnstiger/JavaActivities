    private void registerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerActionPerformed
        String fname = firstname.getText();
        String lname = lastname.getText();
        String uname = username.getText();
        String add = address.getText();
        String pass = String.valueOf(password.getPassword());
        String conf = String.valueOf(confirm.getPassword());
         if(fname.equals("")){
            JOptionPane.showMessageDialog(null, "Please Enter First Name");
        }else if(lname.equals("")){
            JOptionPane.showMessageDialog(null, "Please Enter Last Name");
        }if(uname.equals("")){
           JOptionPane.showMessageDialog(null, "Please Enter Username");
        }else if(pass.equals("")){
           JOptionPane.showMessageDialog(null, "Please Enter Password");
        }else if(!pass.equals(conf)){
           JOptionPane.showMessageDialog(null, "Password's not match!");
        }else if(checkUsername(uname)){
           JOptionPane.showMessageDialog(null, "Username already exist, please try again");
        }else{
            String query = "INSERT INTO `borrowers`(`firstname`, `lastname`, `address`, `username`, `password`) VALUES (?,?,?,?,?)";
            try{
                stmt = MyConnection.getMyConnection().prepareStatement(query);
                stmt.setString(1, fname);
                stmt.setString(2, lname);
                stmt.setString(3, add);
                stmt.setString(4, uname);
                stmt.setString(5, pass);
                if(stmt.executeUpdate()>0){
                    LogInFrame log = new LogInFrame();
                    log.setVisible(true);
                    log.pack();
                    this.dispose();
                }
            }catch(Exception err){
            Logger.getLogger(RegFrame.class.getName()).log(Level.SEVERE, null, err);
            }
        }
    }//GEN-LAST:event_registerActionPerformed

