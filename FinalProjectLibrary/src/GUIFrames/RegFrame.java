 public boolean checkUsername(String username){
        boolean check = false;
        String query = "SELECT * FROM `borrowers` WHERE `username` = ?";
        try {
            stmt = MyConnection.getMyConnection().prepareStatement(query);
            stmt.setString(1, username);
             rs = stmt.executeQuery();
            if(rs.next()){
                check = true;
            }
        } catch (SQLException ex) {
            Logger.getLogger(RegFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        return check;
    }
