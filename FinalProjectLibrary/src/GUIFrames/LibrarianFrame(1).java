    public static void AddRowTable(Object[] datarow){
        DefaultTableModel table = (DefaultTableModel)BookTable.getModel();
        table.addRow(datarow);
    }
     private void filterData(String query){
        DefaultTableModel tab = (DefaultTableModel)BookTable.getModel();
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(tab);
        BookTable.setRowSorter(tr);
        
        tr.setRowFilter(RowFilter.regexFilter(query));
    }
    
